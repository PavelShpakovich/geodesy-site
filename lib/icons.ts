export function getIcon(name?: string, fallback?: string): LucideIcon | undefined {
  if (name && iconMap[name]) {
    return iconMap[name];
  }
  if (fallback && iconMap[fallback]) {
    return iconMap[fallback];
  }
  return undefined;
}

